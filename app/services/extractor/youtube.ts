// Kolay helper – NewPipe: StreamInfo.getInfo(extractor)
export async function getStreamInfo(urlOrId: string): Promise<StreamInfo> {
  const service = new YoutubeService();
  const extractor = service.getStreamExtractor(urlOrId);
  await extractor.fetchPage();
  return extractor.getInfo();
}
